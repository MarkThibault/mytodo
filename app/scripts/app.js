'use strict';

/**
 * @ngdoc overview
 * @name mytodoApp
 * @description
 * # mytodoApp
 *
 * Main module of the application.
 */
angular
  .module('mytodoApp', [
    'ngRoute',
    'ui',
    'LocalStorageModule'
  ])
  .config(function ($routeProvider) {
    $routeProvider
      .when('/', {
        templateUrl: 'views/main.html',
        controller: 'MainCtrl'
      })
      .when('/tutorials', {
        templateUrl: 'views/tutorials.html',
        controller: 'TutorialsCtrl'
      })
      .otherwise({
        redirectTo: '/'
      });
  });
